CREATE TABLE TB_WAREHOUSE_IO_SCREEN (
    WAREHOUSE_IO_ID    NUMBER(19)       GENERATED BY DEFAULT ON NULL AS IDENTITY NOT NULL,
    WAREHOUSE_NAME     VARCHAR2(100)    DEFAULT '' NOT NULL,
    PRODUCT_CODE       VARCHAR2(40)     DEFAULT '' NOT NULL,
    PRODUCT_NAME       VARCHAR2(100)    DEFAULT '' NOT NULL,
    PRODUCT_CATEGORY   VARCHAR2(60)     DEFAULT '' NOT NULL,
    IN_QTY             NUMBER(10)       DEFAULT 0 NOT NULL,
    OUT_QTY            NUMBER(10)       DEFAULT 0 NOT NULL,
    CURRENT_STOCK      NUMBER(10)       DEFAULT 0 NOT NULL,
    CLIENT_NAME        VARCHAR2(100)    DEFAULT '' NOT NULL,
    MANAGER_NAME       VARCHAR2(60)     DEFAULT '' NOT NULL,
    MANAGER_PHONE      VARCHAR2(30)     DEFAULT '' NOT NULL,
    IO_DATE            VARCHAR2(20)     DEFAULT '' NOT NULL,
    STATUS             VARCHAR2(30)     DEFAULT 'DRAFT' NOT NULL,
    MEMO               VARCHAR2(500)    DEFAULT '' NOT NULL,
    DELETED_YN         CHAR(1)          DEFAULT 'N' NOT NULL,
    UPDATED_AT         TIMESTAMP(6)     DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_WAREHOUSE_IO_SCREEN PRIMARY KEY (WAREHOUSE_IO_ID),
    CONSTRAINT CK_WIO_SCREEN_DELETED CHECK (DELETED_YN IN ('Y', 'N'))
);

CREATE INDEX IDX_WIO_ACTIVE ON TB_WAREHOUSE_IO_SCREEN (DELETED_YN, WAREHOUSE_IO_ID DESC);
CREATE INDEX IDX_WIO_UPDATED_AT ON TB_WAREHOUSE_IO_SCREEN (UPDATED_AT, WAREHOUSE_IO_ID);

-- v1.0 투자자 테이블 (레거시·리팩터 공통 스키마 후보)
CREATE TABLE TB_INVESTOR (
    INVESTOR_ID         NUMBER(19)     GENERATED BY DEFAULT ON NULL AS IDENTITY NOT NULL,
    INVESTOR_NAME       VARCHAR2(100)  NOT NULL,
    INVESTOR_GRADE      VARCHAR2(20)   DEFAULT '' NOT NULL,
    TOTAL_AMOUNT        NUMBER(19)     DEFAULT 0 NOT NULL,
    LAST_PRODUCT_NAME   VARCHAR2(100)  DEFAULT '' NOT NULL,
    SCREEN_MEMO         VARCHAR2(500)  DEFAULT '' NOT NULL,
    UPDATED_AT          TIMESTAMP(6)   DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT PK_TB_INVESTOR PRIMARY KEY (INVESTOR_ID)
);

CREATE INDEX IDX_INVESTOR_NAME ON TB_INVESTOR (INVESTOR_NAME);
CREATE INDEX IDX_INVESTOR_UPDATED_AT ON TB_INVESTOR (UPDATED_AT, INVESTOR_ID);
